#define T1_TH  0x4C     // T1重装值高8位: 19456=0x4C00, 高字节=0x4C
#define T1_TL  0x00     // T1重装值低8位: 低字节=0x00

/* --- 定时单位换算常量 --- */
#define TICKS_PER_SEC   20      // 1秒需要的T1中断次数: 1000ms/50ms=20
#define TICKS_PER_MIN   1200    // 1分钟需要的T1中断次数: 20次/秒×60秒=1200

