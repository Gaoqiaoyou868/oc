    natural_tick      = 0;      // 自然风计时归零
    sleep_tick        = 0;      // 睡眠风计时归零
    natural_rand      = 1;      // 自然风随机种子初始化为1
    sleep_decremented = 0;      // ★重置睡眠风降档标志, 允许主循环重置转速
    Fan_UpdateEffectiveSpeed(); // 根据当前风速档位和风类模式计算有效转速
